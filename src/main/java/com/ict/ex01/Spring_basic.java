/*
 	스프링 주요 개념
 		- IoC(Inversion of Controll)
 			--제어의 역전
 			-- 어떤 모듈이 사용할 모듈을 스스로 결정하는 것이 아니라 다른 모듈에게
 			   선택권을 넘겨준다는 것을 의미
	스프링 주요 기능
		- DI(Dependency Injection)
		 	-- 모듈 간의 의존성을 모듈의 외부(컨테이너)에게 주입시키는 기능 , IoC의 한 종류이다.
 			-- 기존 코딩은 작성자가 주체였지만(필요할 때마다 객체 생성),
 			-- 스프링에서는 스프링이 주체가 되어 작성자에게 제공해줄 수 있다.
 			-- 단, 제공해주는 내용은 우리가 주입해줘야한다(xml이나 어노테이션으로 작성).
		- AOP(Aspect Oriented Programming)
			-- 관점 지향 프로그래밍
			-- 핵심적인 부분과 부가적인 부분(공통적으로 사용하는 부분)을 나눠서 사용한다.
				이때도, DI처럼 외부 컨터이너에서 제공해준다.
	 	- MVC(Model-View-Controller)
	 		-- 스프링 자체에서 MVC를 지원한다.
	 
 */
